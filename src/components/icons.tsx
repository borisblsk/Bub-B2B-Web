/* -----------------------------------------------------------------
 * <Icon /> — render by semantic key
 *
 *   <Icon name="home" />
 *   <Icon name="arrow-diagonal1" rotate={45} size={20} />
 *   <Icon name="tick-circle" color="var(--color-success)" />
 * ----------------------------------------------------------------- */
export interface IconComponentProps extends IconProps {
  name: IconName
  /** CSS rotation in degrees (handy for the diagonal arrows). */
  rotate?: number
  /** Hide from screen readers when purely decorative (default: true). */
  decorative?: boolean
  /** ARIA label when the icon is meaningful (sets decorative=false). */
  label?: string
}

export function Icon({
  name,
  rotate,
  decorative = true,
  label,
  size = 24,
  color = 'currentColor',
  style,
  ...rest
}: IconComponentProps) {
  const Cmp = IconMap[name]
  if (!Cmp) {
    if (import.meta.env.DEV) {
      console.warn(`[Icon] Unknown icon name: "${name}"`)
    }
    return null
  }

  const ariaProps = label
    ? { role: 'img' as const, 'aria-label': label }
    : { 'aria-hidden': decorative }

  return (
    <Cmp
      width={size}
      height={size}
      color={color}
      style={rotate ? { transform: `rotate(${rotate}deg)`, ...style } : style}
      {...ariaProps}
      {...rest}
    />
  )
}

